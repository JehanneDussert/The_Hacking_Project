# #{} va permettre dans une string de réaliser un calcul et retourne le résultat du calcul directement dans la str
# les lignes vides ne font rien

# print une str
puts "On va compter le nombre d'heures de travail à THP"
# print une str et calcule le nb d'heures de travail
puts "Travail : #{10 * 5 * 11}"
# print une str et calcule le nb de minutes de travail
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

# print une str
puts "Et en secondes ?"

# print le résultat d'un calcul (int)
puts 10 * 5 * 11 * 60 * 60

# print une str
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

# print le résultat d'un calcul (int)
puts 3 + 2 < 5 - 7

# print une str et le résultat d'un calcul
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
# print une str et le résultat d'un calcul
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

# print une str
puts "Ok, c'est faux alors !"

# print une str
puts "C'est drôle ça, faisons-en plus :"

# print une str et un boolean
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
# print une str et un boolean
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
# print une str et un boolean
